<template>
  <v-form>
    <v-container>
      <v-row>
        <v-col cols="12" md="4">
          <v-text-field v-model="title" counter="10" label="article" required></v-text-field>
        </v-col>
        <v-col cols="12" md="4">
          <v-btn @click="handleSubmit">作成</v-btn>
          <v-btn @click="getApi()">取得</v-btn>
        </v-col>
      </v-row>
    </v-container>
  </v-form>
</template>

<script>
export default {
  data() {
    return {
      title: ""
    };
  },
  methods: {
    handleSubmit() {
      this.$emit("submit", this.title); 
      this.title = "";
    },
    getApi() {
      this.$emit("get"); 
    }
  },
  mounted() {
    this.getApi();
  }
};
</script>

